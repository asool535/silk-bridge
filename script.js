let currentLanguage = 'en';

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    setLanguage('en');
    document.body.classList.remove('rtl');
});

// Change Language
function changeLanguage(lang, button) {
    currentLanguage = lang;
    setLanguage(lang);

    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });

    if (button) {
        button.classList.add('active');
    } else {
        // Fallback: find the button by searching for the language text
        const buttons = document.querySelectorAll('.lang-btn');
        buttons.forEach(btn => {
            if ((lang === 'en' && btn.textContent.trim() === 'EN') ||
                (lang === 'zh' && btn.textContent.includes('中')) ||
                (lang === 'ar' && btn.textContent.includes('العربية'))) {
                btn.classList.add('active');
            }
        });
    }

    // Add RTL for Arabic
    if (lang === 'ar') {
        document.documentElement.lang = 'ar';
        document.body.classList.add('rtl');
    } else {
        document.documentElement.lang = lang;
        document.body.classList.remove('rtl');
    }
}

// Set Language
function setLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
}

// Toggle Menu
function toggleMenu() {
    const navLinks = document.querySelector('.nav-links');
    navLinks.style.display = navLinks.style.display === 'flex' ? 'none' : 'flex';
}

// Toggle WeChat Input
function toggleWechatInput() {
    const wechatInput = document.getElementById('wechat-input');
    const wechatId = document.getElementById('wechat-id');

    if (wechatInput.style.display === 'none') {
        wechatInput.style.display = 'block';
        wechatInput.focus();
    } else {
        if (wechatInput.value) {
            wechatId.textContent = wechatInput.value;
            wechatInput.style.display = 'none';
        }
    }
}

